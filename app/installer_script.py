"""Per-peer PowerShell installer generator + zip packager.

Renders a self-contained .ps1 script with the WireGuard config embedded as
base64. The .ps1 is then wrapped in an AES-256 encrypted zip with an
auto-generated passphrase, suitable for emailing.

Workflow:
  1. UI calls render_install_script(name, conf) -> .ps1 text with conf embedded
  2. UI calls package_install_zip(name, ps1, password) -> (zip bytes, filename)
  3. Browser downloads the zip; UI shows the passphrase to the operator
  4. Operator emails the zip and communicates the passphrase via a
     separate channel (SMS, Signal, in person)
  5. Recipient extracts with 7-Zip (native Windows zip UI does NOT support
     AES-encrypted zips), runs the .ps1, and is on the VPN

The .ps1 itself contains the peer's private key in base64 — the encrypted
zip is the only thing protecting it. This is acceptable for the email-
based distribution flow but means the .ps1 must never be distributed
unencrypted.
"""
from __future__ import annotations

import base64
import io
import re
import secrets
from typing import Tuple

import pyzipper

